public class EPackage {
    /*
    一、package关键字的使用
        1.为了更好的实现项目中类的管理，提供包的概念关
        2.使用package声明类或接口所属的包，声明在源文件的首行
        3.包，属于标识符，遵循标识符的命名规则、规范(xxxyyyzzz)、“见名知意”
        4.每”."一次，就代表一层文件目录。
    补充，同一个包下，不能命名同名的接口、类。
        不同的包下，可以命名同名的接口、类。
     */

    /*
    JDK中主要的包介绍
    1.java.lang----包含一些Java语言的核心类，如String、Math、Integer、System和1.Thread，提供常用功能
    2.java.net----包含执行与网络相关的操作的类和接口。
    3.java.io ----包含能提供多种输入/输出功能的类。
    4.java.util ----包含一些实用工具类，如定义系统特性、接口的集合框架类、使用与日期日历相关的函数。
    5.java.text----包含了一些java格式化相关的类
    6.java.sql----包含了java进行JDBC数据库编程的相关类/接口
    java.awt----包含了构成抽象窗口工具集 (abstract window toolkits)的多个类，这些类被用来构建和管理应用程序的图形用户界面(GUI)。 B/S C/S
     */
}
